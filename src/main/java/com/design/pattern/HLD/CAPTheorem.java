package com.design.pattern.HLD;

public class CAPTheorem {
    /*
            CAP are 3 desirable properties in distributed system with replicated(same/exact) data
            Consistency , Availability , Partition Tolerance
            CAP theorem says we can use only use any 2 property at a given time but not all 3 simultaneously .

            Partition Tolerance simply means can we divide system into 2 or more parts .
            CA - Partition is not allowed , CP , AP .
            In distributed system , we never trade off with Partition, so either CP or AP is possible .

     */
}
